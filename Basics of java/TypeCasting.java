public class TypeCasting {
    public static void main(String[] args) {
        int a = 101;
        char b = (char)a; // type casting from int to char

        double d = 23.55555;
        int e = (int)d; // type casting from double to int

        int f = 23;
        double g = (double)f; // type casting from double to int

        System.out.println(b);
        System.out.println(e);
        System.out.println(g);

    }


}
